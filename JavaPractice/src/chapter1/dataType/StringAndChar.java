package chapter1.dataType;

public class StringAndChar {
    public static void main(String[] args) {
        // char的值只能是單一個字母。且值要用單引號(')才能顯示
        char charValue = 'R';

        // String的值是字串，值要用雙引號(")才能顯示，在所有變數宣告中，只有String是以大寫開頭
        // 注意class名稱不要使用到String
        String stringValue = "R";
    }
}
